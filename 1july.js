let a=parseInt(prompt("enter  1st number"))
let b=parseInt (prompt("enter  2rd number"))
let c=prompt("operation");
switch(c){
    case"+":
    document.write(a+b)
    break;
    case"-":
    document.write(a-b)
    break;
    case"*":
    document.write(a*b)
    break;
    case"/":
    document.write(a/b)
    break;
}
